/**
 * Apply language to all [data-i18n] elements and update UI state.
 * @param {string} lang  "en" or "de"
 */
function setLang(lang) {
  var dict = translations[lang];
  if (!dict) return;

  document.documentElement.lang = lang;

  try { localStorage.setItem("sb-lang", lang); } catch (e) { /* private mode */ }

  var els = document.querySelectorAll("[data-i18n]");
  for (var i = 0; i < els.length; i++) {
    var key = els[i].getAttribute("data-i18n");
    if (dict[key] !== undefined) {
      els[i].innerHTML = dict[key];
    }
  }

  /* Update toggle button text */
  var toggle = document.getElementById("lang-toggle");
  if (toggle) {
    toggle.textContent = lang === "de" ? "EN" : "DE";
  }
}


/* ============================================================
   Bootstrap on DOMContentLoaded
   ============================================================ */
document.addEventListener("DOMContentLoaded", function () {

  /* 1. Detect language */
  var saved = null;
  try { saved = localStorage.getItem("sb-lang"); } catch (e) { /* private mode */ }

  var lang = saved
    || ((navigator.language || navigator.userLanguage || "en").substring(0, 2) === "de" ? "de" : "en");

  /* 2. Apply */
  setLang(lang);

  /* 3. Bind toggle button */
  var toggle = document.getElementById("lang-toggle");
  if (toggle) {
    toggle.addEventListener("click", function () {
      var current = document.documentElement.lang || "en";
      setLang(current === "de" ? "en" : "de");
    });
  }
});
